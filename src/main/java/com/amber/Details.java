package com.amber;

public class Details {

	private String name;
	private String work;
	
	public Details(String n,String w){
		name = n;
		work = w;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	
	
}
